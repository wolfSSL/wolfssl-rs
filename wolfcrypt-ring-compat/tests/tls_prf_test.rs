#[test]
fn tls_prf_derive_vs_derive_with_seed_concat() {
    // derive(label, seed1||seed2, len) must equal
    // derive_with_seed_concatination(label, seed1, seed2, len)
    let secret = &[42u8; 48];
    let label = b"test label";
    let seed1 = &[1u8; 32];
    let seed2 = &[2u8; 32];
    let len = 48;

    let mut combined_seed = Vec::from(seed1.as_ref());
    combined_seed.extend_from_slice(seed2);

    let s1 = Secret::new(&P_SHA256, secret).unwrap();
    let out1 = s1.derive(label, &combined_seed, len).unwrap();

    let s2 = Secret::new(&P_SHA256, secret).unwrap();
    let out2 = s2
        .derive_with_seed_concatination(label, seed1, seed2, len)
        .unwrap();

    assert_eq!(
        out1.as_ref(),
        out2.as_ref(),
        "derive and derive_with_seed_concatination should produce identical output"
    );
}

#[test]
fn tls_prf_empty_secret_fails() {
    assert!(
        Secret::new(&P_SHA256, &[]).is_err(),
        "Empty secret should fail"
    );
}

#[test]
fn tls_prf_zero_output_len_fails() {
    let s = Secret::new(&P_SHA256, &[42u8; 32]).unwrap();
    assert!(
        s.derive(b"label", b"seed", 0).is_err(),
        "Zero output length should fail"
    );
}

#[test]
fn tls_prf_try_into_array() {
    let s = Secret::new(&P_SHA256, &[42u8; 32]).unwrap();
    let derived = s.derive(b"test", b"seed", 32).unwrap();
    let arr: [u8; 32] = derived.try_into().expect("TryInto should succeed for matching length");
    assert_eq!(arr.len(), 32);
}

#[test]
fn tls_prf_try_into_wrong_size_fails() {
    let s = Secret::new(&P_SHA256, &[42u8; 32]).unwrap();
    let derived = s.derive(b"test", b"seed", 32).unwrap();
    let result: Result<[u8; 16], _> = derived.try_into();
    assert!(result.is_err(), "TryInto should fail for wrong length");
}

#[test]
fn tls_prf_different_labels_different_output() {
    let secret = &[42u8; 32];
    let seed = &[7u8; 32];

    let s1 = Secret::new(&P_SHA256, secret).unwrap();
    let out1 = s1.derive(b"label A", seed, 32).unwrap();

    let s2 = Secret::new(&P_SHA256, secret).unwrap();
    let out2 = s2.derive(b"label B", seed, 32).unwrap();

    assert_ne!(
        out1.as_ref(),
        out2.as_ref(),
        "Different labels should produce different output"
    );
}

#[test]
fn tls_prf_different_secrets_different_output() {
    let seed = &[7u8; 32];

    let s1 = Secret::new(&P_SHA256, &[1u8; 32]).unwrap();
    let out1 = s1.derive(b"label", seed, 32).unwrap();

    let s2 = Secret::new(&P_SHA256, &[2u8; 32]).unwrap();
    let out2 = s2.derive(b"label", seed, 32).unwrap();

    assert_ne!(
        out1.as_ref(),
        out2.as_ref(),
        "Different secrets should produce different output"
    );
}

#[test]
fn tls_prf_debug_format() {
    let s = Secret::new(&P_SHA256, &[42u8; 32]).unwrap();
    let debug = format!("{:?}", s);
    // Should contain algorithm info but NOT the secret bytes
    assert!(debug.contains("Secret"), "Debug should contain type name");
}
