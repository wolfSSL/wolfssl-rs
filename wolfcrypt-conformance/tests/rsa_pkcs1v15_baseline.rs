//! or the RSA private-key operation.
//!
//! # How to capture / update vectors
//!
//! Run with `CAPTURE=1` to print all signature bytes in Rust literal format:
//!
//! ```sh
//! CAPTURE=1 cargo test -p wolfcrypt-conformance rsa_pkcs1v15_baseline -- --nocapture 2>&1 | grep -A999 'CAPTURE BEGIN'
//! ```
//!
//! Copy the printed bytes into the `EXPECTED_*` constants below, then re-run
//! without `CAPTURE=1` to verify the hardcoded values are correct.
//!
//! # Key provenance
//!
//! The private keys in `vectors/rsa/priv_2048.der` and `priv_4096.der` are
//! extracted from the Wycheproof `rsa_oaep_*_sha256_mgf1sha256_test.json`
//! files (PKCS#8 outer wrapper stripped to PKCS#1 inner key). These are
//! test keys only — never use them for any real application.

