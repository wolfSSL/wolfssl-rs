// ---------------------------------------------------------------------------
// Determinism self-check: sign the same message twice, must get same bytes
// ---------------------------------------------------------------------------

#[test]
fn pkcs1v15_is_deterministic() {
    let sk =
        RsaPrivateKey::from_pkcs1_der(PRIV_2048_DER).expect("failed to load 2048-bit private key");
    let sig1 = sk
        .sign_pkcs1v15_with_digest(MSG, RsaDigest::Sha256)
        .expect("first sign failed");
    let sig2 = sk
        .sign_pkcs1v15_with_digest(MSG, RsaDigest::Sha256)
        .expect("second sign failed");
    assert_eq!(
        sig1.as_ref(),
        sig2.as_ref(),
        "PKCS#1v1.5 produced different bytes on two calls — this is a serious bug!"
    );
}

// ---------------------------------------------------------------------------
// Different messages produce different signatures
// ---------------------------------------------------------------------------

#[test]
fn pkcs1v15_different_messages_differ() {
    let sk =
        RsaPrivateKey::from_pkcs1_der(PRIV_2048_DER).expect("failed to load 2048-bit private key");
    let sig1 = sk
        .sign_pkcs1v15_with_digest(b"message one", RsaDigest::Sha256)
        .expect("sign failed");
    let sig2 = sk
        .sign_pkcs1v15_with_digest(b"message two", RsaDigest::Sha256)
        .expect("sign failed");
    assert_ne!(
        sig1.as_ref(),
        sig2.as_ref(),
        "Different messages produced identical signatures — hash collision or bug"
    );
}

