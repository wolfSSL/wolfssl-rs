fn sign_and_assert(sk: &RsaPrivateKey, digest: RsaDigest, expected: &[u8], label: &str) {
    let sig_result = sk.sign_pkcs1v15_with_digest(MSG, digest);
    let sig = sig_result.unwrap_or_else(|e| {
        panic!("{label}: sign_pkcs1v15_with_digest({digest:?}) failed: {e:?}");
    });

    // Verify the signature with the corresponding public key (independent round-trip).
    let pk = sk.public_key();
    let sig_obj = RsaPkcs1v15Signature::try_from(sig.as_ref())
        .expect("failed to create RsaPkcs1v15Signature from sig bytes");
    pk.verify_pkcs1v15_with_digest(MSG, &sig_obj, digest)
        .unwrap_or_else(|e| {
            panic!(
                "{label}: verify_pkcs1v15_with_digest({digest:?}) failed on own signature: {e:?}"
            );
        });

    let capture_mode = std::env::var("CAPTURE").map(|v| v == "1").unwrap_or(false);

    if capture_mode {
        // Print bytes in Rust literal format for easy copy-paste.
        print!("  static {label}: &[u8] = &[");
        for (i, b) in sig.as_ref().iter().enumerate() {
            if i % 16 == 0 {
                print!("\n    ");
            }
            print!("0x{b:02x},");
        }
        println!("\n  ];");
    } else if !expected.is_empty() {
        // Byte-exact comparison — only active once expected bytes are populated.
        assert_eq!(
            sig.as_ref(),
            expected,
            "{label}: PKCS#1v1.5 signature bytes changed! \
             This indicates a regression in the signing implementation.\n\
             If this is intentional (e.g., migration to native shims), \
             regenerate expected bytes with CAPTURE=1.",
        );
    }
    // If expected is empty and CAPTURE is off: sign + verify round-trip only.
}

